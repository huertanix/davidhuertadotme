        <h3><a href="https://github.com/huertanix/pewpewping">Pew Pew Ping! (2012)</a></h3>
        <p>An interactive art piece which sends a TCP packet burst to MPAA.org when you shoot it with Nerf guns, using an Arduino, Wifi shield and pressure sensor. Created for <a href="http://arthackday.net/gaffta/" target="_new">Art Hack Day San Francisco</a>.</p>
        
        <h3><a href="http://www.davidhuerta.me/tumblrtimes/">The Tumblr Times (2012)</a></h3>
        <p>A printable HTML5 newspaper featuring content aggregated from the Tumblr API. Created as my project for The New York Times's 2012 <a href="http://open.blogs.nytimes.com/timesopen-schedule/" target="_new">TimesOpen</a> Hack Day.</p> 
        
        <h3><a href="https://github.com/huertanix/txtvote">txtvote (2012)</a></h3>
        <p>Twilio-powered text message voting application built on Ruby on Rails designed for use with SMS shortcodes.</p>
        
        <h3><a href="https://github.com/huertanix/supervillmin">Supervillmin (2012)</a></h3>
        <p>A supervillain-friendly port of Douglas Crockford's JSMin, using Ruby and heavy abuse of regular expressions. Originally designed for replacing JSMin's use in <a href="https://crypto.cat">CryptoCat</a> during <a href="http://datatransparency.wsj.com/" target="_new">Data Transparency Weekend</a>.</p>
        
        <h3><a href="https://github.com/huertanix/bot-ranch">Bot Ranch (2012)</a></h3>
        <p>A web/large-screen-in-midtown companion piece to <a href="http://captaindaylight.com/" target="_new">Paul Christophe</a>'s <a href="http://arthackday.net/project/8/" target="_new">Bot Zoo</a> during <a href="http://arthackday.net/319scholes/" target="_new">Art Hack Day</a>.  HTML5 and Ruby is abused to intentionally create the best worst looking site for a large screen whilst assaulting the audience with British SMS spam messages in a giant pink Papyrus font.</p>
        
        <h3><a href="http://www.davidhuerta.me/tagfight/">Tag Fight! (2011)</a></h3>
        <p>My project for <a href="http://photohackday.org/" target="_new">Photo Hack Day</a> 2011.  Fight over what kind of photos to display full-screen on a screen of any size (like, say, <a href="http://techcrunch.com/2011/08/03/nyc-photo-hack-day-is-coming-nasdaq-to-feature-winners-in-times-square/" target="_new">the Nasdaq display on Times Square</a>) by texting tags.  Ingredients include HTML5, CSS3, Twilio, and Flickr's <del>push</del> API.</p>
        
        <h3><a href="http://userscripts.org/scripts/show/97717" target="_new">EightSixTwo (2011)</a></h3>
        <p>A Greasemonkey userscript for distraction affliction correction. Adds a 30 second delay to the display of any web page you open in your browser, as per the <a href="http://blog.xkcd.com/2011/02/18/distraction-affliction-correction-extensio/" target="_new">beckoning of Randal Munroe</a> of <a href="http://www.xkcd.com" target="_new">xkcd</a> fame.</p>
        
        <h3><a href="https://github.com/huertanix/overflow-touch" target="_new">overflow-touch (2011)</a></h3>
        <p>A small JavaScript library to display a glyph for iOS device users indicating they can use two fingers to scroll in an area that would otherwise have a scrollbar on other devices.</p>
        
        <h3><a href="http://www.heatsynclabs.org/wiki/Loldialer" target="_new">loldialer (2010)</a></h3>
        <p>Programmed Ruby script and Arduino microcontroller to create a phone-booth-ish interface to an easy, push-button system to prank call people with a variety of troll-tastic sound clips, including <a href="http://www.youtube.com/watch?v=dQw4w9WgXcQ" target="_new">this one</a>.  Premiered at Maker Faire 2011, trolling 460+ people in its wake.</p>
        
        <h3><a href="http://userscripts.org/scripts/show/65329">Lady Freeman (2010)</a></h3>
        <p>A Greasemonkey userscript to replace all instances of "Lady Gage" with "Gordon Freeman" for a celebrity-free cybersauce.
        
        <h3><a href="http://hayst.ac" target="_new">Hayst.ac (2008)</a></h3>
        <p>Created a proof-of-concept web application and Greasemonkey script which obfuscates a user's search history in a variety of search engines, including Google, where the things you "clicked" on a search results page are also obfuscated.</p>
        
        <h3><a href="http://www.youtube.com/watch?v=Fap00jsOBV8" target="_new">Duck Hunt at Shmoocon Hacker Arcade (2007)</a></h3>
        <p>Refitted a Pentium III PC to run Debian, MAME, pygame and cwiid to create an Wiimote-controlled arcade machine that took quarters in exchange for gameplay.  Created with assistance from Ryan MacArthur, the Shmoo Group, and Kenshoto.</p>
      
