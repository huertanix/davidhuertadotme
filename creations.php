            <h3><a href="http://hayst.ac" target="_new">Hayst.ac (2008)</a></h3>
            <p>Created a proof-of-concept web application and Greasemonkey script which obfuscates a user's search history in a variety of search engines, including Google, where the things you "clicked" on a search results page are also obfuscated.</p>
            <h3><a href="http://www.youtube.com/watch?v=Fap00jsOBV8" target="_new">Duck Hunt at Shmoocon Hacker Arcade (2007)</a></h3>
            <p>Refitted a Pentium III PC to run Debian, MAME, pygame and cwiid to create an Wiimote-controlled arcade machine that took quarters in exchange for gameplay.  Created with assistance from Ryan MacArthur, the Shmoo Group, and Kenshoto.</p>
            
